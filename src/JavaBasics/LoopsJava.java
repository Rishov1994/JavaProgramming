package JavaBasics;

public class LoopsJava {

	public static void main(String[] args) {
		
		//WhileLoop
		//Print Top 10 Integers
		
		int i=3;
		while (i<=10) {
			System.out.println(i);
			i=i+3;
		}
		System.out.println("*********");
		//ForLoop

		int j=20;
		for (j=40;j>=1;j=j-2) {
			System.out.println(j);
		}
	}

}
